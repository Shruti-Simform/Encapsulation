class EmployeeE {
    //Private fields
    private int emplID;
    private String name;

    //Getter and setter methods
    public void setEmplID(int emplID) {
        this.emplID = emplID;
    }

    public int getEmplId() {
        return emplID;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }
}